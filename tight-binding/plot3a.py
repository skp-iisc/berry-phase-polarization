from pol_alpha import *
import matplotlib.pyplot as plt

T1, T2 = 80, 120
print(f"Simulating for T = {T1}...")
t2, P_dyn_80 = calc_dyn_pol_alpha(T1, t_max=160, N_k=200)
_, P_ad_80 = calc_ad_pol_alpha(T1, t_max=160, N_k=200)
print(f"Simulating for T = {T2}...")
_, P_dyn_120 = calc_dyn_pol_alpha(T2, t_max=160, N_k=200)
_, P_ad_120 = calc_ad_pol_alpha(T2, t_max=160, N_k=200)
print("Simulation done.")

plt.figure(figsize=(8, 5))
mask = (t2 >= 70)
plt.plot(t2[mask], P_dyn_80[mask], '-', label='Dynamic (T = 80)')
plt.plot(t2[mask], P_dyn_120[mask], '-.', lw=1.8, label='Dynamic (T = 120)')
plt.plot(t2[mask], P_ad_80[mask], 'k--', lw=1, label='Adiabatic')
plt.plot(t2[mask], P_ad_120[mask], 'k--', lw=1)
plt.xlabel('Time', fontsize=16)
plt.ylabel('Polarization', fontsize=16)
plt.xlim(70, 150)
plt.ylim(0.992, 1.004)
plt.xticks([80, 120], fontsize=14)
plt.yticks([0.992, 0.996, 1.000], fontsize=14)
plt.legend(loc='lower right', fontsize=14, frameon=True)
plt.tick_params(direction='in', top=True, right=True)
plt.tight_layout()
plt.savefig('figs/prb69fig3a.png', dpi=300)
plt.show()
print("Polarization evolution is plotted.")
